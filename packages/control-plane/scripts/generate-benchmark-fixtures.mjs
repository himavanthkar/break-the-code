import { readdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, "../../..");
const tasksDir = join(repoRoot, "benchmark/data/tasks");
const metadataDir = join(repoRoot, "benchmark/internal/metadata");
const outFile = join(__dirname, "../src/benchmarks/fixtures.ts");

const JSON_SUFFIX = /\.json$/u;
const NON_ALNUM = /[^a-zA-Z0-9]+/gu;

const ident = (prefix, filename) => {
  const base = filename.replace(JSON_SUFFIX, "");
  return `${prefix}_${base.replace(NON_ALNUM, "_")}`;
};

const taskFiles = (await readdir(tasksDir))
  .filter((f) => f.endsWith(".json"))
  .sort((a, b) => a.localeCompare(b));
const metadataFiles = (await readdir(metadataDir))
  .filter((f) => f.endsWith(".json"))
  .sort((a, b) => a.localeCompare(b));

const taskImports = taskFiles.map(
  (f) =>
    `import ${ident("task", f)} from "../../../../benchmark/data/tasks/${f}" with {\n  type: "json",\n};`
);
const metadataImports = metadataFiles.map(
  (f) =>
    `import ${ident("meta", f)} from "../../../../benchmark/internal/metadata/${f}" with {\n  type: "json",\n};`
);

const out = `// Generated by \`pnpm --dir packages/control-plane generate:benchmark-fixtures\` — do not edit by hand.
${[...taskImports, ...metadataImports].join("\n")}

export const benchmarkDatasetFixtures = {
  metadata: [
${metadataFiles.map((f) => `    ${ident("meta", f)},`).join("\n")}
  ],
  tasks: [
${taskFiles.map((f) => `    ${ident("task", f)},`).join("\n")}
  ],
} as const;
`;

await writeFile(outFile, out);
process.stdout.write(
  `Wrote ${taskFiles.length} task(s) and ${metadataFiles.length} metadata file(s) to ${outFile}\n`
);
